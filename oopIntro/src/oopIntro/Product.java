package oopIntro;

public class Product {
	//encapsulation:
	private int id;
	private String name;
	private double unitPrice;
	private String detail;
	private double discount;//indirim
	//private double unitPriceAfterDiscount;//indirimden sonraki oran,buna artýk gerek yok sadece okuyoruz çünkü silinebilir
	
	public Product()
	{
		//constractýr blogu
		//bu blogu oluþturmak istediðimizde olmasýný istrdiðimiz
		//þeyleri bunun içine yazarýz
		//burasý biz yazmasakta var
		System.out.println("ben çalýþtým");
	}
	
	public Product(int id,String name,double unitPrice,String detail,
			double discount) 
	{
		//neleri bana yollýcaný parametrelere yazdýk
		//eþitlemek için
		//this();//yukardaki parametresiz constractýrý çalýþtýr demek
		super();
		this.id=id;
		this.name=name;
		this.unitPrice=unitPrice;
		this.detail=detail;
		this.discount =discount;
		//this bu classtaki demek yani aþaðýda tanýmlananlarý buraya eþitliyor
		//pythondaki self ile ayný
	}
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getUnitPrice() {
		return unitPrice;
	}

	public void setUnitPrice(double unitPrice) {
		this.unitPrice = unitPrice;
	}

	public String getDetail() {
		return detail;
	}

	public void setDetail(String detail) {
		this.detail = detail;
	}

	public double getDiscount() {
		return discount;
	}

	public void setDiscount(double discount) {
		this.discount = discount;
	}

	public double getUnitPriceAfterDiscount() {
		return this.unitPrice-(this.unitPrice*this.discount/100);
	}

	
	
	
	


}
